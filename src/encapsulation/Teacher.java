package encapsulation;

public class Teacher {
	
	private int teacherID;
	private String teacherName;
	private String teacherSSN;
	private String teacherDOB;
	
	
	public int getTeacherID() {
		return teacherID;
	}
	public void setTeacherID(int teacherID) {
		this.teacherID = teacherID;
	}
	public String getTeacherName() {
		return teacherName;
	}
	public void setTeacherName(String teacherName) {
		this.teacherName = teacherName;
	}
	public String getTeacherSSN() {
		return teacherSSN;
	}
	public void setTeacherSSN(String teacherSSN) {
		this.teacherSSN = teacherSSN;
	}
	public String getTeacherDOB() {
		return teacherDOB;
	}
	public void setTeacherDOB(String teacherDOB) {
		this.teacherDOB = teacherDOB;
	}
	
	
	

}
